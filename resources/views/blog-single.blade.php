            <p>Dan pada zaman Tuhan Yesus, juga demikian, tercatat Yesus masa kecilnya juga melakukan perjalanan dari Nazaret ke Yerusalem bersama Yusuf dan Maria di mana Yesus diam di Yerusalem Bait Suci berdiskusi dengan para guru Taurat dan akhirnya ditemukan oleh orang tua-Nya. Jadi inilah praktek ziarah di alkitab.</p>
            <p>Bagaimana dengan kita yang hidup di zaman modern ini? Ziarah telah menjadi sebuah perjalanan wisata rohani yang mengkombinasikan elemen ziarah dengan wisata. Umat Tuhan yang berziarah ke Tanah Perjanjian Israel biasanya memakai jasa Agent Tour untuk mengurus visa, penerbangan, konsumsi, khotbah, renungan, logistik maupun rute dan seluk beluk lain nya dalam perjalanan ziarah ini. Maka sangat penting untuk para peziarah memahami makna ziarah, paket tour ziarah yang di tawarkan dan tentu nya peran Agent Tour sangatlah penting karena rancangan rute ziarah ditentukan oleh Agent Tour ini.</p>
            <p>Ziarah Rohani sebetulnya adalah sebuah perjalanan ibadah (worship) karena memang dari dulunya, Tuhan memerintahkan umat Israel melakukan ziarah 3 kali dalam setahun untuk kerohanian mereka. Maka dari itu, perjalanan ziarah haruslah menitikberatkan kepada ibadah dan kerohanian yang bertumpu pada Firman Tuhan di berbagai lokasi ziarah. Lokasi akan memberikan konteks kepada perikop alkitab.</p>

