            <p>Perjalanan ziarah adalah suatu hal yang mulia dan baik sekali untuk dilakukan. Namun seberapa banyak anda paham akan seluk beluk perjalanan ziarah? Nah artikel ini ditujukan untuk para umat Tuhan agar bisa mengerti dan memaksimalkan perjalanan Ziarah Holyland ke Tanah Perjanjian Israel.
              <br/>
              Apakah itu ziarah?
              <br/>
              Berziarah dalam arti umum adalah berkunjung ke tempat khusus atau situs suci untuk berdoa, beribadah dan mengenang keluarga, leluhur, tokoh penting atau pemimpin yang di hormati dan di ikuti peziarah tersebut. Dalam ziarah rohani, pada umumnya peziarah berkunjung ke Israel dan negara-negara sekitar yang juga menjadi tanah atau wilayah terjadinya kisah-kisah yang terdapat dalam alkitab.
              <br/>
              Pada zaman dahulu kala, saat Musa memimpin bangsa Israel keluar dari tanah perbudakan di Mesir, maka Tuhan memerintahkan perayaan-perayaan penting bagi kaum Israel, yakni :
              <br/>
              1. Hari Raya Paskah. (The Feast of the Passover)
              <br/>
              2. Hari Raya Roti Tidak Beragi. (The Feast of Unleavened Bread)
              <br/>
              3. Hari Raya Buah Sulung. (The Feast of Firstfruits)
              <br/>
              4. Hari Raya Tujuh Minggu (Shavuot / The Feast of the Weeks / The Feast of Pentecost)
              <br/>
              5. Hari Raya Peniupan Serunai (The Feast of the Blowing of Trumpets / Rosh Hashanah)
              <br/>
              6. Hari Raya Puasa dan Pendamaian (The Feast of Expiation / Yom Kippur)
              <br/>
              7. Hari Raya Pondok Daun (The Feast of Tabernacles / Sukkot)</p>
            <p>
              <img src="images/history2.jpg" alt="" class="img-fluid">
            </p>
            <p>Dari ketujuh Hari Raya ini, kemudian Tuhan berfirman melalui Musa kepada bangsa Israel : tiga kali setahun setiap orang laki-laki di antaramu harus menghadap hadirat TUHAN, Allahmu, ke tempat yang akan dipilih-Nya, yakni pada hari raya Roti Tidak Beragi; pada hari raya Tujuh Minggu dan pada hari raya Pondok Daun. Janganlah ia menghadap hadirat TUHAN dengan tangan hampa, tetapi masing-masing dengan sekedar persembahan, sesuai dengan berkat yang diberikan kepadamu oleh TUHAN, Allahmu. </p>
            <p>Setelah bangsa Israel menduduki tanah Kanaan dan berdiam di tanah milik masing-masing suku berdasarkan pembagian, maka sebanyak tiga kali setahun semua pria harus datang menghadap TUHAN Allah di tempat yang telah dipilih-Nya. Pada masa awal, tempat ini adalah Shekhem kemudian Shiloh di mana terletak Tabut Perjanjian yang mewakili hadirat TUHAN, setelah Bait Suci pertama di dirikan Raja Salomo, maka Yerusalem menjadi pusat ibadah orang-orang Israel.</p>
            <p>Perjalanan orang Israel dari desa dan kota masing-masing menuju Yerusalem untuk menaati perintah Tuhan Allah bagi mereka sebanyak 3 kali setahun untuk 3 perayaan itu lah yang kemudian di kenal sebagai perjalanan ziarah. Dan orang-orang ini di sebut Peziarah. Mereka akan datang menuju Yerusalem sesuai waktu yang telah ditetapkan Tuhan, dengan membawa korban ataupun hasil ladang mereka untuk dipersembahkan (istilah kita saat ini beribadah) kepada Tuhan di Bait-Nya dan merayakan sesuai perintah Tuhan. Bahkan ada bagian khusus Kitab Mazmur berisi nyanyian dan doa para peziarah sewaktu mereka dalam perjalanan ke Yerusalem.</p>
            <p>
              <img src="images/history3.jpg" alt="" class="img-fluid">
