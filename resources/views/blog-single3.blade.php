            <p>"Kamu tidak boleh melakukan perzinahan". Menarik untuk dicatat bahwa segera setelah perintah yang menyatakan kesucian hidup manusia, datanglah orang yang menyatakan kesucian pernikahan. Urutan ilahi dari Sepuluh Perintah yang diberikan oleh Tuhan setelah anak-anak Israel dibebaskan dari Mesir. Kelompok pertama adalah tentang hubungan kita sebagai manusia dengan Tuhan. Yang kedua adalah tentang hubungan kita satu sama lain sebagai manusia. Dalam pengelompokan kedua ini, hubungan antara laki-laki dan istri adalah jenis dan asal mula dari semua hubungan selanjutnya. Tidak ada yang lebih penting bagi tatanan sosial selain hubungan yang di atasnya dibangun semua yang berikutnya. Setiap bangsa, keluarga dan pasangan yang percaya bahwa hubungan pernikahan dapat terganggu dengan impunitas sedang menabur benih kehancurannya sendiri. Ketidaksetiaan seperti menjatuhkan batu ke dalam kolam. Riak tidak hanya tinggal di tengah, tapi juga menyebarkan efek dan konsekuensinya dengan cara yang menghancurkan. Tuhan menciptakan pernikahan dan keluarga, jadi itu harus dihormati demi-Nya. Pada dasarnya, perintah ini mengatakan bahwa segala bentuk ekspresi seksual (menurut kedua jenis kelamin) di luar ikatan pernikahan adalah penyimpangan (baik dalam pengertian yang paling dasar maupun yang paling lengkap dari istilah tersebut) dan dilarang.</p>
            <p>Pernahkah kita bertanya-tanya mengapa masalah perzinahan ini begitu menyedihkan dan merusak di mata Tuhan? Karena melanggar keutuhan nazar, yaitu satu kesatuan yang dirancang untuk menggambarkan dan mengekspresikan gambaran ketuhanan. Persatuan dan keakraban suami dan istri tidak tunduk pada keanehan manusia tetapi disengaja. Karena Allah menciptakan manusia menurut gambar-Nya, laki-laki dan perempuan Dia menciptakan mereka (Kej 1:27). Meskipun Alkitab dengan jelas menyatakan bahwa melajang adalah pemberian Tuhan kepada beberapa orang (Mat 19:12 dan 1 Kor 7:32), pernikahan antara seorang pria dan seorang wanita benar-benar mencerminkan kesatuan dalam hubungan dalam Tritunggal. Kami menemukan sebelumnya bahwa Tuhan sangat bersemangat sehingga tidak ada yang akan mengganggu atau mencairkan kesatuan esensial-Nya. Apa yang Dia rencanakan untuk diri-Nya, Dia juga rencanakan untuk kita. Kita dibuat menurut gambar dan perzinahan-Nya, ketidakpercayaan mengganggu dan menghancurkan keesaan yang dirancang untuk menjadi ekspresi dari gambar ilahi. Kita bisa mulai memahami mengapa Tuhan begitu jelas memperingatkan kita terhadap perilaku merusak ini. Perintah ini ditujukan terhadap kerusakan yang diakibatkan oleh perselingkuhan dalam perkawinan, yang merupakan pemutusan hak sakral ikatan perkawinan, tetapi rohnya dengan tegas memperingatkan terhadap kenajisan. Begitu gagasan diterima bahwa pernikahan mencerminkan kesatuan dan kesatuan gambar ilahi, maka menjadi jelas bahwa seks pra-nikah bukanlah seperti yang awalnya dirancang Allah.</p>
            <p>Keinginan dan rencana Tuhan untuk keintiman dalam hubungan pernikahan dan dengan Diri-Nya sebagai cerminan dari hubungan Tritunggal adalah kunci untuk memahami pentingnya dan masalah di balik perintah ini. Namun, kesetiaan dalam pernikahan saat ini berada di bawah serangan filosofis dan teologis langsung. Kita hidup dalam masyarakat yang jenuh dengan seks. Media memberitahu kita dalam seribu satu cara: "kamu akan bernafsu, dalam pikiran dan perkataan dan perbuatan". Buku dan artikel ditulis untuk mendukung gagasan bahwa seks selalu ada di pikiran kita dan itu normal. Oleh karena itu pelanggaran terhadap perintah ini tersebar luas di masyarakat kita saat ini, di sebagian besar negara di planet ini. Tetapi dengan ketergantungan sepenuhnya kepada Tuhan, menimba dari-Nya anugerah dan kuasa yang kita butuhkan untuk menjalani hidup sesuai dengan keinginan-Nya, kita dapat mematuhi perintah ini dan membantu kita dan keluarga kita berkembang. Banyak guru Kristen mengutamakan pengampunan bagi mereka yang jatuh ke dalam dosa ini, tetapi tidak menghabiskan banyak waktu untuk menjelaskan konsekuensinya. Kita tidak boleh berhenti berbicara tentang pengampunan, tetapi kita juga tidak boleh berhenti berbicara tentang kerusakan yang disebabkan. Andai saja kita mengambil lebih banyak waktu untuk memahami efek dan konsekuensi perzinahan, mungkin kita tidak perlu menghabiskan begitu banyak waktu untuk membantu orang menemukan pengampunan untuk itu. Kerusakan destruktif yang timbul karena pelanggaran terhadap perintah ini tidak pernah dapat diremehkan.</p>
           

